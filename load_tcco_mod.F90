MODULE LOAD_TCCO_MOD

USE INTDYNSL_MOD, ONLY : TCCO

INTERFACE LOAD
MODULE PROCEDURE LOAD_TCCO
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TCCO (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCCO), INTENT (OUT) :: YD

READ (KLUN) YD%M_RLON
READ (KLUN) YD%M_RLAT
READ (KLUN) YD%M_RQX
READ (KLUN) YD%M_RQY
READ (KLUN) YD%NDIM
END SUBROUTINE


END MODULE
