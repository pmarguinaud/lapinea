READ (KLUN) LNESPZERO
IF (LNESPZERO) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NESPZERO (IL1(1):IU1(1)))
  READ (KLUN) YD%NESPZERO
ENDIF
READ (KLUN) LNESM0G
IF (LNESM0G) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NESM0G (IL1(1):IU1(1)))
  READ (KLUN) YD%NESM0G
