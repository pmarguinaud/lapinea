  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NCPLM (IL1(1):IU1(1)))
  READ (KLUN) YD%NCPLM
ENDIF
READ (KLUN) LNCPL2N
IF (LNCPL2N) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NCPL2N (IL1(1):IU1(1)))
  READ (KLUN) YD%NCPL2N
ENDIF
