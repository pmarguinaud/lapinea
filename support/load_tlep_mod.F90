IF (LRLEPINM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLEPINM (IL1(1):IU1(1)))
  READ (KLUN) YD%RLEPINM
ENDIF
READ (KLUN) LNESM0
IF (LNESM0) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NESM0 (IL1(1):IU1(1)))
  READ (KLUN) YD%NESM0
