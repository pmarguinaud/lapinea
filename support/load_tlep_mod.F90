MODULE LOAD_TLEP_MOD

USE YEMLAP, ONLY : TLEP

INTERFACE LOAD
MODULE PROCEDURE LOAD_TLEP
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TLEP (KLUN, YD)

IMPLICIT NONE
