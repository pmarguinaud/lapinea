IF (LNCPL4N) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NCPL4N (IL1(1):IU1(1)))
  READ (KLUN) YD%NCPL4N
ENDIF
READ (KLUN) LNCPLN
IF (LNCPLN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NCPLN (IL1(1):IU1(1)))
  READ (KLUN) YD%NCPLN
ENDIF
READ (KLUN) LRLEPDIN
IF (LRLEPDIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLEPDIN (IL1(1):IU1(1)))
  READ (KLUN) YD%RLEPDIN
ENDIF
READ (KLUN) LRLEPINN
IF (LRLEPINN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLEPINN (IL1(1):IU1(1)))
  READ (KLUN) YD%RLEPINN
ENDIF
READ (KLUN) LRLEPDIM
IF (LRLEPDIM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLEPDIM (IL1(1):IU1(1)))
  READ (KLUN) YD%RLEPDIM
