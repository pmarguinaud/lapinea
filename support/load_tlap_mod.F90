MODULE LOAD_TLAP_MOD

USE YOMLAP, ONLY : TLAP

INTERFACE LOAD
MODULE PROCEDURE LOAD_TLAP
END INTERFACE

CONTAINS

SUBROUTINE LOAD_TLAP (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TLAP), INTENT (OUT) :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LMYMS, LNASM0, LNASM0G, LNASN0, LNSE0L, LNSPZERO, LNVALUE, LRLAPDI, LRLAPIN
READ (KLUN) LNASN0
IF (LNASN0) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NASN0 (IL1(1):IU1(1)))
  READ (KLUN) YD%NASN0
ENDIF
READ (KLUN) LNASM0
IF (LNASM0) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NASM0 (IL1(1):IU1(1)))
  READ (KLUN) YD%NASM0
ENDIF
READ (KLUN) LNASM0G
IF (LNASM0G) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NASM0G (IL1(1):IU1(1)))
  READ (KLUN) YD%NASM0G
ENDIF
READ (KLUN) LNVALUE
IF (LNVALUE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NVALUE (IL1(1):IU1(1)))
  READ (KLUN) YD%NVALUE
ENDIF
READ (KLUN) LMYMS
IF (LMYMS) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%MYMS (IL1(1):IU1(1)))
  READ (KLUN) YD%MYMS
ENDIF
READ (KLUN) LNSPZERO
IF (LNSPZERO) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NSPZERO (IL1(1):IU1(1)))
  READ (KLUN) YD%NSPZERO
ENDIF
READ (KLUN) LNSE0L
IF (LNSE0L) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NSE0L (IL1(1):IU1(1)))
  READ (KLUN) YD%NSE0L
ENDIF
READ (KLUN) LRLAPDI
IF (LRLAPDI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLAPDI (IL1(1):IU1(1)))
  READ (KLUN) YD%RLAPDI
ENDIF
READ (KLUN) LRLAPIN
IF (LRLAPIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLAPIN (IL1(1):IU1(1)))
  READ (KLUN) YD%RLAPIN
ENDIF
END SUBROUTINE


END MODULE
