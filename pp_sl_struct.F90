SUBROUTINE _PP_SL_STRUCT_(YDSL)

USE EINT_MOD

IMPLICIT NONE

TYPE(SL_STRUCT)   ,INTENT(INOUT) :: YDSL

INTEGER :: I

PRINT *, YDSL%CVER

DO I = 1, SIZE (YDSL%NSLSTA)
  PRINT *, YDSL%NSLSTA (I)
ENDDO

DO I = 1, SIZE (YDSL%NSLONL)
  PRINT *, YDSL%NSLONL (I)
ENDDO

DO I = 1, SIZE (YDSL%NLOENG)
  PRINT *, YDSL%NLOENG (I)
ENDDO

END
